package org.example;

public class CNCMachine {
    public String maker;
    public String countryOfOrigin;
    public String model;
    public double length;
    public double width;
    public double height;
    public double weight;
    public double tableWorkArea;
    public int maxPowerConsumption;
    public int workingVoltage;
    public String commandLanguage;
    public String operatingSystem;
    public String getMaker() {
        return maker;
    }
    public void setMaker(String maker) {
        this.maker = maker;
    }
    public String getCountryOfOrigin() {
        return countryOfOrigin;
    }
    public void setCountryOfOrigin(String countryOfOrigin) {
        this.countryOfOrigin = countryOfOrigin;
    }
    public String getModel() {
        return model;
    }
    public void setModel(String model) {
        this.model = model;
    }
    public double getLength() {
        return length;
    }
    public void setLength(double length) {
        this.length = length;
    }
    public double getWidth() {
        return width;
    }
    public void setWidth(double width) {
        this.width = width;
    }
    public double getHeight() {
        return height;
    }
    public void setHeight(double height) {
        this.height = height;
    }
    public double getWeight() {
        return weight;
    }
    public void setWeight(double weight) {
        this.weight = weight;
    }
    public double getTableWorkArea() {
        return tableWorkArea;
    }
    public void setTableWorkArea(double tableWorkArea) {
        this.tableWorkArea = tableWorkArea;
    }
    public int getMaxPowerConsumption() {
        return maxPowerConsumption;
    }
    public void setMaxPowerConsumption(int maxPowerConsumption) {
        this.maxPowerConsumption = maxPowerConsumption;
    }
    public int getWorkingVoltage() {
        return workingVoltage;
    }
    public void setWorkingVoltage(int workingVoltage) {
        this.workingVoltage = workingVoltage;
    }
    public String getCommandLanguage() {
        return commandLanguage;
    }
    public void setCommandLanguage(String commandLanguage) {
        this.commandLanguage = commandLanguage;
    }
    public String getOperatingSystem() {
        return operatingSystem;
    }
    public void setOperatingSystem(String operatingSystem) {
        this.operatingSystem = operatingSystem;
    }
}
